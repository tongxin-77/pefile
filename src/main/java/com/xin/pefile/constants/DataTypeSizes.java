package com.xin.pefile.constants;

/**
 * @author tongxin
 * @date 2024/4/17 23:00
 */
public interface DataTypeSizes {
    /**
     * 字(word)，两个字节
     **/
    int WORD = 2;
    /**
     * 双字(double word)，四个字节
     **/
    int DWORD = 4;
    /**
     * 字节(byte)，一个字节
     **/
    int BYTE = 1;
    /**
     * 短整型(short)，两个字节
     **/
    int SHORT = 2;
    /**
     * 整型(int)，四个字节
     **/
    int INT = 4;
    /**
     * 长整型(long)，四个字节
     **/
    int LONG = 4;

}
